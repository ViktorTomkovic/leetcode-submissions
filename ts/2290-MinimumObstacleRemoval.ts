import { Deque } from "@datastructures-js/deque";
function minimumObstacles(grid: number[][]): number {
	const WALL = 1;
	const rows = grid.length;
	const columns = grid[0].length;
	const gridSize = rows * columns;
	const path = Array.from(
		{ length: rows },
		() => new Array(columns).fill(gridSize + 1),
	);
	const visited = Array.from(
		{ length: rows },
		() => new Array(columns).fill(false),
	);
	class PathNode {
		row: number;
		column: number;
		length: number;
		constructor(row: number, column: number, length: number) {
			this.row = row;
			this.column = column;
			this.length = length;
		}
	}
	const unprocessed = new Deque<PathNode>();
	const addToUnprocessed = (
		row: number,
		column: number,
		length: number,
	) => {
		if (
			row < 0 || row >= rows || column < 0 || column >= columns ||
			path[row][column] <= length || visited[row][column]
		) {
			return;
		}
		if (grid[row][column] == WALL) {
			unprocessed.pushBack(new PathNode(row, column, length + 1));
			visited[row][column] = true;
		} else {
			unprocessed.pushFront(new PathNode(row, column, length));
			visited[row][column] = true;
		}
	};
	unprocessed.pushFront(
		new PathNode(rows - 1, columns - 1, grid[rows - 1][columns - 1]),
	);
	while (path[0][0] > gridSize && !unprocessed.isEmpty()) {
		const { row, column, length } = unprocessed.popFront();
		path[row][column] = length;
		addToUnprocessed(row - 1, column, length);
		addToUnprocessed(row, column - 1, length);
		addToUnprocessed(row + 1, column, length);
		addToUnprocessed(row, column + 1, length);
	}
	return path[0][0];
}


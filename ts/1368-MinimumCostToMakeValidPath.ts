import { MinPriorityQueue } from "npm:@datastructures-js/priority-queue@5.4.0";
function minCost(grid: number[][]): number {
	const rows = grid.length;
	const columns = grid[0].length;
	const verticesSize = rows * columns;
	const edgesSize = verticesSize * 4;
	const edges = new Array(edgesSize);
	const flattened = grid.flat();
	for (let i = 0; i < verticesSize; i++) {
		edges[4 * i + 0] = flattened[i] == 1 ? 0 : 1;
		edges[4 * i + 1] = flattened[i] == 2 ? 0 : 1;
		edges[4 * i + 2] = flattened[i] == 3 ? 0 : 1;
		edges[4 * i + 3] = flattened[i] == 4 ? 0 : 1;
	}

	function sPath(
		verticesSize: number,
		edges: number[],
		columns: number,
		endVertex: number,
		queue: MinPriorityQueue<number>,
		visited: Set<number>,
	): number {
		while (!queue.isEmpty()) {
			console.log(endVertex, queue.front());
			const { priority: unvisitedLength, element: vertex } = queue
				.dequeue();
			if (vertex == endVertex) {
				return unvisitedLength;
			}
			if (visited.has(vertex)) {
				continue;
			}
			visited.add(vertex);
			let vertexTo = vertex + 1;
			if (
				!visited.has(vertexTo) &&
				0 <= vertexTo &&
				vertexTo < verticesSize &&
				vertex % columns != columns - 1
			) {
				queue = queue.enqueue(
					vertexTo,
					unvisitedLength + edges[4 * vertex + 0],
				);
			}
			vertexTo = vertex - 1;
			if (
				!visited.has(vertexTo) &&
				0 <= vertexTo &&
				vertexTo < verticesSize &&
				vertex % columns != 0
			) {
				queue = queue.enqueue(
					vertexTo,
					unvisitedLength + edges[4 * vertex + 1],
				);
			}
			vertexTo = vertex + columns;
			if (
				!visited.has(vertexTo) &&
				0 <= vertexTo &&
				vertexTo < verticesSize
			) {
				queue = queue.enqueue(
					vertexTo,
					unvisitedLength + edges[4 * vertex + 2],
				);
			}
			vertexTo = vertex - columns;
			if (
				!visited.has(vertexTo) &&
				0 <= vertexTo &&
				vertexTo < verticesSize &&
				vertex % columns != 0
			) {
				queue = queue.enqueue(
					vertexTo,
					unvisitedLength + edges[4 * vertex + 3],
				);
			}
		}

		return -1;
	}

	function shortestPath(
		verticesSize: number,
		edges: number[],
		columns: number,
		startVertex: number,
		endVertex: number,
	): number {
		const visited = new Set<number>();
		const queue = new MinPriorityQueue<number>();
		queue.enqueue(startVertex, 0);
		return sPath(
			verticesSize,
			edges,
			columns,
			endVertex,
			queue,
			visited,
		);
	}
	return shortestPath(
		verticesSize,
		edges,
		columns,
		0,
		verticesSize - 1,
	);
}

